#dictionay

#implicit

#syntax:variableName={key:value,key:value}

userDetails={'firstName':'Navya','lastName':'Kolakani','Email':'navya@gmail.com'}
print(type(userDetails))

#Explicit

#syntax:variableName=dict({key:value,key:value})

userDetails=dict({'firstName':'Navya','lastName':'Kolakani','Email':'navya@gmail.com'})
print(type(userDetails))

#datatype/Variable Annotation

#syntax:variableName:dict={key:value,key:value}

userDetails:dict={'firstName':'Navya','lastName':'Kolakani','Email':'navya@gmail.com'}
print(type(userDetails))